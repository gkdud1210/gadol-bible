// ── Wikipedia 요약 API 호출 (CORS 지원, 제한적) ─────────────
// Wikimedia REST API: https://en.wikipedia.org/api/rest_v1/page/summary/{title}
// 한국어: https://ko.wikipedia.org/api/rest_v1/page/summary/{title}

async function fetchWikiSummary(title) {
  try {
    const encoded = encodeURIComponent(title);
    const url = `https://ko.wikipedia.org/api/rest_v1/page/summary/${encoded}`;
    const resp = await fetch(url, { headers: { Accept: 'application/json' } });
    if (!resp.ok) return null;
    const data = await resp.json();
    return {
      extract: data.extract || '',
      thumbnail: data.thumbnail?.source || null,
      pageUrl: data.content_urls?.mobile?.page || `https://ko.wikipedia.org/wiki/${encoded}`,
    };
  } catch {
    return null;
  }
}

// ── OSM Tile 미리보기 URL ─────────────────────────────────
// OpenStreetMap Static Map (via staticmap API via osm-staticmap or tile)
function buildOsmPreviewUrl(lat, lon, zoom = 13) {
  // OSM 타일 이미지 URL (단일 타일)
  const z = zoom;
  const x = Math.floor((lon + 180) / 360 * Math.pow(2, z));
  const y = Math.floor((1 - Math.log(Math.tan(lat * Math.PI / 180) + 1 / Math.cos(lat * Math.PI / 180)) / Math.PI) / 2 * Math.pow(2, z));
  return `https://tile.openstreetmap.org/${z}/${x}/${y}.png`;
}

// OSM 지도 링크 URL
function buildOsmLinkUrl(lat, lon, zoom = 13) {
  return `https://www.openstreetmap.org/?mlat=${lat}&mlon=${lon}#map=${zoom}/${lat}/${lon}`;
}

